function ipairs_remove(tbl)
   local i = 0
   local remove = function()
      table.remove(tbl, i)
      i = i - 1
   end
   return function()
      i = i + 1
      if tbl[i] then
         return i, tbl[i], remove
      end
      return nil
   end
end

