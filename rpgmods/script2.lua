local toSend = {}
function grabDownload()
	if not client then return end
	toSend[client] = {}
	for i,v in pairs(gOthers) do
		toSend[client][#toSend[client]+1] = v
	end	
	
	for i,v in pairs(gReplace) do
		if v.texture then 
			toSend[client][#toSend[client]+1] = "replace/"..v.texture
		end
		if v.model then 
			toSend[client][#toSend[client]+1] = "replace/"..v.model
		end
		if v.col then 
			toSend[client][#toSend[client]+1] = "replace/"..v.col
		end		
	end
	
	for i,v in pairs(gMaps) do
		toSend[client][#toSend[client]+1] = v
	end
	triggerClientEvent(client, "receiveFirst", client, gReplace, toSend[client])
end
addEvent("grabDownload", true)
addEventHandler("grabDownload", getRootElement(), grabDownload)

function checkFileSizes(data)
	if not client then return end
	if not toSend[client] then return end
	for i,v in ipairs(data) do
		if toSend[client][i] then
			if fileExists(toSend[client][i]) then
				local file = fileOpen(toSend[client][i])
				if file then
					if fileGetSize(file) == v then
						toSend[client][i] = "remove"
					end
					fileClose(file)
				end
			end
		end
	end
	
	for i, v, remove in ipairs_remove(toSend[client]) do
	   if v == "remove" then remove() end
	end
	
	triggerClientEvent(client, "receiveSecond", client, toSend[client])
end
addEvent("checkFileSizes", true)
addEventHandler("checkFileSizes", getRootElement(), checkFileSizes)

function requestFile(name, size)
	if not client then return end
	if not toSend[client] then return end
	if not name then return end
	if not fileExists(name) then return end
	local file = fileOpen(name, true)
	if file then
		if fileGetSize(file) == size then
			triggerLatentClientEvent ( client, "receiveFile", 494304,false, client, name, "piemel" )
		else
			local buffer = ""
			buffer = fileRead(file, fileGetSize ( file ))		
			triggerLatentClientEvent ( client, "receiveFile", 494304,false, client, name, buffer )
		end
		fileClose(file)
	end
end
addEvent("requestFile", true)
addEventHandler("requestFile", getRootElement(), requestFile)


